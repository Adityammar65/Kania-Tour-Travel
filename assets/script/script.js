document.addEventListener("DOMContentLoaded", function () {
  const forms = {
    'form-sewa': () => {
      const category = document.getElementById('category').value;
      const alertMsg = `Anda telah menyewa ${category} Kami akan menghubungi anda untuk info lebih lanjut`;
      alert(alertMsg);
    },
    'form-kontak': () => {
      const alertMsg = `Terima kasih atas kepercayaan Anda kepada Kania Tour & Travel. Kami akan segera menghubungi Anda untuk memberikan informasi lebih lanjut.`;
      alert(alertMsg);
    }
  };

  // Ensure elements exist before adding event listeners
  const formElements = ['form-sewa', 'form-kontak'];
  formElements.forEach(formId => {
    const form = document.getElementById(formId);
    if (form) { // Check if element exists
      form.addEventListener('submit', function (e) {
        e.preventDefault();
        forms[formId]();
      });
    } else {
      console.error(`Element with id '${formId}' not found in the DOM.`);
    }
  });
});

